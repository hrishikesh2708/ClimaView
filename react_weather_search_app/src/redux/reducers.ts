export const dataReducer = (
  state = initialState,
  action: DataActionTypes
): DataState => {
  switch (action.type) {
    case FETCH_AUTOCOMPLETE_DATA:
      return state;
    case FETCH_AUTOCOMPLETE_DATA_SUCCESS:
      return {
        ...state,
        autocomplete: {
          autocompleteStatus: true,
          autocompletePayload: action?.payload,
          autocompleteStateSuggestion: action?.payload?.predictions
            .map((prediction) => prediction?.terms[1]?.value),
          autocompleteSuggestion: action?.payload?.predictions
            .map(
              (prediction) =>
                prediction.terms.find((term) => term.offset === 0)?.value
            )
            .filter(Boolean),
        },
      };
    case FETCH_AUTOCOMPLETE_DATA_FAILURE:
      return { ...state, error: action.error };
    case TOGGLE_DETAILS_RESULTS_TAB:
      const initialboolValue = state?.detailsResultsSection;
      return {
        ...state,
        detailsId: action?.input,
        detailsResultsSection: !initialboolValue,
      };
    case RETURN_RESULTS_TAB:
      return {
        ...state,
        detailsId: action?.input,
        detailsResultsSection: false,
      };
    case TOGGLE_RESULTS_FAV_TAB:
      return {
        ...state,
        toggleResultsFav: action?.input,
      };
    case FETCH_IPINFO_DATA:
      return { ...state, loading:true, progress: 0};
    case FETCH_IPINFO_DATA_SUCCESS:
      return {
        ...state,
        ipinfoData: action?.payload,
        stateName: action?.payload?.region,
        cityName: action?.payload?.city,
        countryName: action?.payload?.country,
        latLong: action?.payload?.loc,
      };
    case FETCH_IPINFO_LATLNG_SUCCESS:
      return {
        ...state,
        latLong: action?.payload,
      }
    case FETCH_IPINFO_DATA_FAILURE:
      return { ...state, loading: false, error: action.error };
    case FETCH_GEOCODE_DATA:
      return { ...state, loading: true , progress: 0};
    case FETCH_GEOCODE_DATA_SUCCESS:
      return {
        ...state,
        latLong: `${action?.payload?.results[0]?.geometry?.location?.lat}, ${action?.payload?.results[0]?.geometry?.location?.lng}`,
        geocodeData: action?.payload,
        formCityname: action?.cityInput,
        formatedAddress: action?.payload?.results[0]?.formatted_address,
        cityName: action?.payload?.results[0]?.address_components.find(
          (component) => component.types[0] === "locality"
        )?.long_name! || "",
        stateName: action?.payload?.results[0]?.address_components.find(
          (component) => component.types[0] === "administrative_area_level_1"
        )?.long_name!,
        countryName: action?.payload?.results[0]?.address_components.find(
          (component) => component.types[0] === "country"
        )?.long_name!,
      };
    case FETCH_GEOCODE_DATA_FAILURE:
      return { ...state, loading: false, error: action.error };
    case FETCH_WEATHER_DATA:
      return { ...state, loading: true};
    case FETCH_WEATHER_DATA_SUCCESS:
      const today = new Date(action?.payload[1]?.startTime);
      const fiveDaysLater = new Date(today);
      fiveDaysLater.setDate(today.getDate() + 5);
      return {
        ...state,
        loading: false,
        dailyWeatherData: action?.payload[0],
        hourlyWeatherData: action?.payload[1],
        minMaxtemperatureData: action?.payload[0]?.intervals.map((interval) => {
          const dateTimestamp = new Date(interval.startTime).getTime();
          const temperatureMin = interval.values.temperatureMin;
          const temperatureMax = interval.values.temperatureMax;
          return [dateTimestamp, temperatureMin, temperatureMax];
        }),
        MetogramWeatherValues: {
          humidity: action?.payload[1]?.intervals
            .filter((interval) => {
              const intervalDate = new Date(interval.startTime);
              return intervalDate <= fiveDaysLater;
            })
            .map((interval) => {
              const x = new Date(interval.startTime).getTime();
              const y = Math.trunc(interval.values.humidity);
              return { x, y };
            }),
          winds: action?.payload[1]?.intervals
            .filter((interval) => {
              const intervalDate = new Date(interval.startTime);
              return intervalDate <= fiveDaysLater;
            })
            .map((interval) => {
              const x = new Date(interval.startTime).getTime();
              const value = interval.values.windSpeed;
              const direction = interval.values.windDirection;
              return { x, value, direction };
            }),
          temperatures: action?.payload[1]?.intervals
            .filter((interval) => {
              const intervalDate = new Date(interval.startTime);
              return intervalDate <= fiveDaysLater;
            })
            .map((interval) => {
              const x = new Date(interval.startTime).getTime();
              const y = interval.values.temperature;
              return { x, y };
            }),
          pressures: action?.payload[1]?.intervals
            .filter((interval) => {
              const intervalDate = new Date(interval.startTime);
              return intervalDate <= fiveDaysLater;
            })
            .map((interval) => {
              const x = new Date(interval.startTime).getTime();
              const y = interval.values.pressureSeaLevel;
              return { x, y };
            }),
        },
      };
    case FETCH_WEATHER_DATA_FAILURE:
      return { ...state, loading: false, error: action.error };
    case FETCH_FAVORITES_DATA:
      return {...state};
    case FETCH_FAVORITES_DATA_SUCCESS:
      let isEmpty = action?.payload.length <= 0 ? true : false;
      return {
        ...state,
        loading: false,
        favoritesData: action?.payload,
        isFavoritesEmpty: isEmpty,
      };
    case FETCH_FAVORITES_DATA_FAILURE:
      return { ...state, loading: false, error: action.error };
    case DELETE_FAVORITES_DATA:
      return state;
    case DELETE_FAVORITES_DATA_SUCCESS:
      let empty = action?.payload.length <= 0 ? true : false;
      return {
        ...state,
        favoritesData: action?.payload,
        isFavoritesEmpty: empty,
      };
    case DELETE_FAVORITES_DATA_FAILURE:
      return { ...state, loading: false, error: action.error };
    case ADD_FAVORITES_DATA:
      return state;
    case ADD_FAVORITES_DATA_SUCCESS:
      let x = action?.payload.length <= 0 ? true : false;
      return {
        ...state,
        favoritesData: action?.payload,
        isFavoritesEmpty: x,
      };
    case ADD_FAVORITES_DATA_FAILURE:
      return { ...state, loading: false, error: action.error };
    case NO_RECORDS_FOUND:
      return { ...state, loading: false, noRecodeFound: true };
    case RECORDS_FOUND:
      return { ...state, loading: false, noRecodeFound: false };
    case PROGRESS_UPDATE:
      return { ...state, progress: action?.payload }
    default:
      return state;
  }
};
